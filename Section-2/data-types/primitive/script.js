// Data Types in Javascript
// Primitives and Non-Primitives

// Primitives
// String, Number, Boolean, Null, Undefined, Symbol

// String Data Type
let firstName = "Seher";
let lastName = "Siddique";
console.log(firstName + " " + lastName);
let num = "10";
console.log(typeof num);
// Number Data Type
let age = 25;
let height = 5.0;
console.log(age);
console.log(height);

// Boolean Data Type
let isStudent = true;
let isEmployed = false;
console.log(typeof isStudent);
console.log(isEmployed);
let x = 20 > 10;
console.log(x); //true

// undefined Data Type
let number1;
console.log(number1); //undefined->value
console.log(typeof number1); //undefined

// null Data Type
let number2 = null;
console.log(number2);
console.log(typeof number2); //object
console.log(null == undefined); //true

